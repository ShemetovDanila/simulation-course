using System;

namespace WinFormsApp1
{
    // ══════════════════════════════════════════════════════════════════════════
    //  ПАРАМЕТРЫ СИМУЛЯЦИИ (изменяются динамически через слайдеры)
    // ══════════════════════════════════════════════════════════════════════════

    /// <summary>
    /// Единый объект параметров, передаваемый каждой клетке при вычислении
    /// следующего состояния. Изменение любого поля мгновенно влияет на симуляцию.
    /// </summary>
    public class SimulationParams
    {
        // ── Основные правила (правила 1–4 из схемы) ─────────────────────────

        /// <summary>
        /// [f] Вероятность спонтанного возгорания дерева за 1 тик.
        /// (молния / человеческий фактор — правило 3)
        /// </summary>
        public double LightningProb { get; set; } = 0.000035;

        /// <summary>
        /// [p] Вероятность роста нового растения в пустой клетке за 1 тик.
        /// (правило 4)
        /// </summary>
        public double GrowthProb { get; set; } = 0.006;

        // ── Дополнительное правило: Пепел (удобрение) ───────────────────────

        /// <summary>
        /// Бонус к вероятности роста на пепле или рядом с ним.
        /// Пепел обогащает почву → растения появляются быстрее.
        /// </summary>
        public double AshFertilizeBonus { get; set; } = 0.024;

        /// <summary>Длительность состояния пепла в тиках перед зарастанием.</summary>
        public int AshDuration { get; set; } = 20;

        // ── Воспламенение (правило 2) ────────────────────────────────────────

        /// <summary>
        /// Базовый шанс воспламенения (0..1).
        /// Трава: base × 1.3 | Молодые деревья: base × 1.0 | Взрослые: base × 0.55
        /// </summary>
        public double IgnitionBase { get; set; } = 0.45;

        /// <summary>
        /// Бонус к вероятности воспламенения за каждого горящего соседа сверх первого.
        /// Чем больше огня вокруг — тем быстрее загорается.
        /// </summary>
        public double IgnitionBonusPerNeighbor { get; set; } = 0.06;

        // ── Степени огня: дальнее распространение ───────────────────────────

        /// <summary>
        /// Шанс поджечь клетку ЧЕРЕЗ ОДНУ для огня 1-го уровня (молодые деревья).
        /// Небольшой радиус — маленькое пламя.
        /// </summary>
        public double LongRangeLevel1 { get; set; } = 0.12;

        /// <summary>
        /// Шанс поджечь клетку ЧЕРЕЗ ОДНУ для огня 2-го уровня (взрослый лес).
        /// Большой пожар бросает искры далеко.
        /// </summary>
        public double LongRangeLevel2 { get; set; } = 0.30;

        // ── Длительность горения по уровням ─────────────────────────────────

        /// <summary>Трава горит быстро (4 тика).</summary>
        public int FireDurationL0 { get; set; } = 4;

        /// <summary>Молодые деревья горят дольше (6 тиков).</summary>
        public int FireDurationL1 { get; set; } = 6;

        /// <summary>Взрослый лес горит долго и жарко (9 тиков).</summary>
        public int FireDurationL2 { get; set; } = 9;

        // ── Пороги возраста растений (для роста) ────────────────────────────

        /// <summary>Минимальный возраст травы (тики) перед возможным ростом.</summary>
        public int GrassGrowthAge { get; set; } = 38;

        /// <summary>Минимальный возраст молодого дерева (тики) перед ростом.</summary>
        public int YoungTreeGrowthAge { get; set; } = 62;
    }
}